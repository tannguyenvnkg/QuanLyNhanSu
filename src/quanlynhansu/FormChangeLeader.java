    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        try {
            model();
            ChucNang cn = new ChucNang();
            String tenphong  = cboPhong.getSelectedItem().toString(); // lấy tên phòng
            int a = tableLeader.getSelectedRow();
            String NewIDLeader = model.getValueAt(a, 0).toString(); // lấy mã leader mới
            cn.ChangeRoomLeader_FormChangeLeader(NewIDLeader,tenphong);
            txtNewLeader.setText("");
            JOptionPane.showMessageDialog(null, "Đổi Leader Thành Công");
        } catch (SQLException ex) {
            Logger.getLogger(FormChangeLeader.class.getName()).log(Level.SEVERE, null, ex);
        }catch (Exception e){
            if(txtNewLeader.getText().equals("")) JOptionPane.showMessageDialog(null, "Vui Lòng Chọn Leader");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

