    private void cboPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboPhongActionPerformed
        try {
            ChucNang cn = new ChucNang();
            String tenphong = (String) cboPhong.getSelectedItem();
            txtOldLeader.setText(cn.TenLeader(tenphong));// lấy tên phòng để tìm leader
            model();
            cn.ShowLeader_FormChangeLeader(model,tenphong);
        } catch (SQLException ex) {
            Logger.getLogger(FormPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cboPhongActionPerformed

    private void tableLeaderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableLeaderMouseClicked
        model();
        int a = tableLeader.getSelectedRow();
        txtNewLeader.setText(model.getValueAt(a, 1).toString());
    }//GEN-LAST:event_tableLeaderMouseClicked

